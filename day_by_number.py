print("Enter a day number")
num = input()
if(int(num) == 1):
    print("Monday")
elif(int(num) == 2):
    print("Tuesday")
elif(int(num) == 3):
    print("Wednessday")
elif(int(num) == 4):
    print("Thursday")
elif(int(num) == 5):
    print("Friday")
elif(int(num) == 6):
    print("Saturday")
elif(int(num) == 0):
    print("Sunday")
else:
    print("Invalid input")